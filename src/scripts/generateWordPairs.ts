// Generate many pairs by random grouping
function generateLargePairs(words: string[], targetPairs: number) {
  const pairs: [string, string][] = [];
  const shuffled = [...words].sort(() => Math.random() - 0.5);

  let index = 0;
  while (pairs.length < targetPairs) {
    const word1 = shuffled[index % shuffled.length];
    const word2 = shuffled[(index + 1) % shuffled.length];

    if (word1 !== word2) {
      pairs.push([word1, word2]);
    }

    index += 2;
  }

  return pairs;
}

