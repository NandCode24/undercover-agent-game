const pairsPerCategory = 220; // 7 categories Ã— 220 = 1540 pairs

Object.entries(categories).forEach(([category, words]) => {
  finalPairs[category] = generateLargePairs(words, pairsPerCategory);
});

fs.writeFileSync(
  "generatedWordPairs.json",
  JSON.stringify(finalPairs, null, 2),
);

